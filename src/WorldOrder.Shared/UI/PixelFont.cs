    public PixelFont(ArtLibrary art) => _art = art;

    public Vector2 Measure(string text, int scale)
    {
        return new Vector2(text.Length * 6 * scale, 7 * scale);
    }

    public void Draw(SpriteBatch batch, string text, Vector2 position, Color color, int scale = 2)
    {
        text = text.ToUpperInvariant();
        var x = (int)position.X;
        var y = (int)position.Y;
        foreach (var ch in text)
        {
            var glyph = _glyphs.TryGetValue(ch, out var g) ? g : _glyphs['?'];
            for (var row = 0; row < 7; row++)
            {
                for (var col = 0; col < 5; col++)
                {
                    if (glyph[row][col] == '1')
                    {
                        batch.Draw(_art.Pixel, new Rectangle(x + col * scale, y + row * scale, scale, scale), color);
                    }
                }
            }
            x += 6 * scale;
        }
    }

    public void DrawShadow(SpriteBatch batch, string text, Vector2 position, Color color, int scale = 2)
    {
        Draw(batch, text, position + new Vector2(scale, scale), Color.Black * 0.65f, scale);
        Draw(batch, text, position, color, scale);
    }
